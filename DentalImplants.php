        <h1 class="img">Information About Dental Implants Dentistry</h1>
        <div class="box1">

            <p>A dental implant (also known as an endosseous implant or fixture) is a prosthesis that interfaces with the
                bone of the jaw or skull to support a dental prosthesis such as a crown, bridge, denture, or facial
                prosthesis or to act as an orthodontic anchor. The basis for modern dental implants is a biologic process
                called osseointegration, in which materials such as titanium or zirconia form an intimate bond to bone.
                The implant fixture is first placed so that it is likely to osseointegrate, then a dental prosthetic is
                added. A variable amount of healing time is required for osseointegration before either the dental
                prosthetic (a tooth, bridge or denture) is attached to the implant or an abutment is placed which will
                hold a dental prosthetic/crown.</p>

                Success or failure of implants depends on the health of the person receiving the treatment, drugs which affect the chances
                of osseointegration, and the health of the tissues in the mouth. 
                <p>The amount of stress that will be put on the implant and fixture 
                during normal function is also evaluated. Planning the position and number of implants is key to the long-term health of the prosthetic 
                since biomechanical forces created during chewing can be significant. The position of implants is determined by the position and angle of 
                adjacent teeth, by lab simulations or by using computed tomography with CAD/CAM simulations and surgical guides called stents. The prerequisites 
                for long-term success of osseointegrated dental implants are healthy bone and gingiva. Since both can atrophy after tooth extraction, pre-prosthetic 
                procedures such as sinus lifts or gingival grafts are sometimes required to recreate ideal bone and gingiva.</p>

                <p>The final prosthetic can be either fixed, where a person cannot remove the denture or teeth from their mouth, or removable, where they can remove the prosthetic.
                In each case an abutment is attached to the implant fixture. Where the prosthetic is fixed, the crown, bridge or denture is fixed to the abutment either with lag
                 screws or with dental cement. Where the prosthetic is removable, a corresponding adapter is placed in the prosthetic so that the two pieces can be secured together.
                The risks and complications related to implant therapy divide into those that occur during surgery (such as excessive bleeding or nerve injury), those that occur in 
                the first six months (such as infection and failure to osseointegrate) and those that occur long-term (such as peri-implantitis and mechanical failures).
                 In the presence of healthy tissues, a well-integrated implant with appropriate biomechanical loads can have 5-year plus survival rates from 93 to 98 percent 
                 and 10 to 15 year lifespans for the prosthetic teeth.Long-term studies show a 16- to 20-year success (implants surviving without complications or revisions)
                  between 52% and 76%, with complications occurring up to 48% of the time.</p>

