            <h1>Root Canal Specialist</h1>
            <hr>
            <p>When you refer to medical doctors, you know that includes doctors whose specialties range from pediatrics to orthopedics. The same is true for dental doctors. Endodontists specialize in root canal therapy and related nerve tissue procedures.</p>
            <p>Every tooth has a delicate inner pulp chamber that houses nerves and blood vessels to support sustainability and growth. If trauma or bacteria disturbs this fragile area, an infection can arise, eliminating essential nutrients for the tooth. In addition, endodontists are skilled at providing adequate anesthesia, minimizing anxiety, and completing treatment promptly.</p>
            <p>Endodontist accreditation requires 2-3 years of advanced education and training upon completing four years of dental school. General dentists are trained to perform root canals; however, many refer to an endodontist. One of the reasons is that endodontists are trained to use chairside microscopes. An endodontist can magnify the tooth up to 25x the original size, allowing exceptional visualization.</p>
            <h1>Endodontist benefits</h1>
            <hr>
            <ol>1.Advanced training and education</ol>
            <ol>2.Microscope magnification used during treatment</ol>
            <ol>3.Unique tools to navigate curved roots</ol>
            <ol>4.Conscious sedation is possible</ol>
            <ol>5.Faster treatment compared to general dentist</ol>
            <ol>6.Expert anesthetic administration</ol>
            <ol>7.Focused practice around root canals</ol>

            <h1>Common procedures</h1>
            <hr>
            <p>An endodontist’s most common procedures are initial endodontic therapy, endodontic retreatment, and apicoectomies. Root canal therapy requires complete removal of the affected nerve tissue, bacterial debridement, and gutta-percha application; however, they may be prone to re-infection if bacteria penetrate the inner chamber and progress into the roots. The bacteria will proliferate and exit the end of the root leading to infection and possible abscess. The old root canal may need to be removed, debrided, and retreated if this occurs.</p>
            <p>The last technique is an apicoectomy, which requires removing the infection at the root base, bypassing the existing root canal. The disease is accessed through the gum and bone tissue along the side of the jaw. Let’s take a closer look at these unique procedures.</p>

            <h1>Initial endodontic therapy</h1>
            <hr>
            <p>As we’ve discussed before, if bacteria have entered the nerve space, also known as the pulp chamber, removal is necessary to eliminate the bacteria and prevent future microbial invasion. After an adequate anesthetic is administered, the assistant will place a rubber tent around the planned tooth to limit contamination and avoid irrigation solution entering the mouth.</p>
            <p>Next, the clinician will enter the nerve space and remove the nerve. You should be adequately numb; however, immediately let your clinician know if you feel any pain. The clinician will then enter each root and remove the remaining nerve tissue, carefully navigating the twist and turns.</p>
            <p>Next, mechanical and chemical antibacterial irrigants will be applied to the roots to verify adequate debridement. After this, the dentist will use gutta-percha material into each root to seal off bacteria and prevent microbial re-infection. You may be wondering how the clinician knows where to stop at the end of the root. They use a specialized file machine to calculate and measure each root in millimeters.</p>
            <p>Root canals can be tedious and technique sensitive, so it’s best to have an endodontist perform this procedure when in doubt. After completion, a temporary filling will be placed, and the clinician will advise you to return to your general dentist for crown fabrication. Although rare, let’s take a closer look at the most common reasons root canals fail.</p>

            <h1>Root canal failure</h1>
            <hr>
            <ol>1.Bacterial contamination during procedure</ol>
            <ol>2.Infected nerve tissue left in roots</ol>
            <ol>3.Contamination while waiting for final crown</ol>
            <ol>4.Lack of completion of final crown</ol>
            <ol>5.Underfilled root canal/poor technique</ol>
            <ol>6.Cavity forms under crown leading to re-infection</ol>
            <ol>7.Bacterial leakage around temporary filling</ol>
            <ol>8.Fractured instrument in root</ol>
            <ol>9.Missed root canal during treatment</ol>
            <p>Your endodontist will be focused on limiting these points of failure. Root canal therapy has been around for many years and has stood the test of time for safety and efficacy. There are approximately 15 million root canals performed each year in the U.S alone.</p>

            <h1>Endodontic retreatment</h1>
            <hr>
            <p>The leading cause of root canal failure is bacterial persistence within the root canal or coronal leakage after completion of the final restoration. Therefore, it’s essential to weigh the pros and cons of root canal retreatment versus an extraction. The literature reports a success rate ranging from 80-88% for endodontic retreatment; however, many factors are considered. These factors include clinical expertise, minimal root curvature, adequate isolation, diagnosis, and therapeutic success. In addition, general dentists are rarely trained to perform root canal re-treatments, so it’s advised to have an endodontist perform it.</p>
            <p>After adequate anesthesia and isolation, the previous crown is carefully removed. At this point, the clinician will have a better idea if the root canal failed because of decay or due to bacteria persistence within the root canal. Removing the previous root canal requires time. Next, the clinician will remove the remaining bacteria using mechanical and chemical means, then re-apply gutta-percha.</p>
            <p>Finally, they’ll place a temporary filling and send you back to your general dentist. There are scenarios where the endodontist will be able to access the root canal through the old crown if a root canal is advised for reasons other than decay under the crown. If this occurs, a permanent filling will be used to seal off bacteria after treatment completion.</p>

            <h1>Apicoectomy</h1>
            <hr>
            <p>If an endodontist sees an infection on the x-ray and the root canal appears acceptable, they may consider an apicoectomy, which requires drilling through the side of the jaw, at the level of the root tip. The endodontist must accurately predict where the root tip is located compared to the gingival margin and adjacent teeth.</p>
            <p>After accessing the root tip beneath the bone, the clinician will remove the bacteria and debride the area to remove the source of the infection. Material is then applied to seal the root canal and prevent bacteria contamination. Sometimes bone graft material is placed around the root if a bony defect is present. Sutures will be used to encourage proper healing. According to reports, there’s a 90% success rate for apicoectomies due to advances in technology and techniques.</p>

            <h1>Considerations</h1>
            <hr>
            <p>Initial root canal therapy has a high 85-95% success rate and allows the patient to preserve their natural tooth rather than have the tooth extracted. If extraction is desired, the most common method for replacing teeth is with a dental implant. This procedure is considered the “gold standard” for replacing teeth. There are other options including, a removable partial denture if multiple teeth need to be replaced. The last option is a permanent bridge that secures the adjacent teeth. Before committing to any treatment, discuss what option is best for you with your provider.</p>

            <h1>Final thoughts</h1>
            <p>Root canal therapy is an effective procedure that can bring pain relief and heal a damaged tooth. Have you recently been diagnosed with needing a root canal? Are you aware of the alternative options? My Dental Advocate’s team of board-certified dentists can provide a second opinion on your planned treatment. We look forward to bringing you peace of mind by verifying your treatment plan, suggesting an alternative, or answering your questions.</p>

            
        
