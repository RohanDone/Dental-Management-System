                <p> A little girl's trip to the pediatric dentist examining children's teeth.
                Pediatric (also paediatric or pædiatric) dentists promote the dental health of children as well as serve as educational resources for parents. 
                It is recommended by the American Academy of Pediatric Dentistry (AAPD) and the American Academy of Pediatrics (AAP) that a dental visit occurs 
                after the presence of the first tooth or by a child's first birthday. The AAPD has said that it is important to establish a comprehensive and 
                accessible ongoing relationship between the dentist and patient – referring to this as the patient's "dental home".This is because early oral 
                examination aids in the detection of the early stages of tooth decay. Early detection is essential to maintain oral health, modify aberrant habits, 
                and treat as needed and as simply as possible. Additionally, parents are given a program of preventive home care (brushing, flossing and fluorides), 
                a caries risk assessment, information on finger, thumb, and pacifier habits, and may include advice on preventing injuries to the mouth and teeth of children,
                 diet counseling, and information on growth and development.</p>
           
