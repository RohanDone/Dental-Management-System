        <h1>Cosmetic Dentistry</h1>
        <hr>
            <p>Cosmetic Dentistry Is Generally Used To Refer To Any Dental Work That Improves The Appearance (Though Not Necessarily The Functionality) Of Teeth, Gums And/Or Bite. It Primarily Focuses On Improvement In Dental Aesthetics In Color, Position, Shape, Size, Alignment And Overall Smile Appearance.Many Dentists Refer To Themselves As "Cosmetic Dentists" Regardless Of Their Specific Education, Specialty, Training, And Experience In This Field. This Has Been Considered Unethical With A Predominant Objective Of Marketing To Patients.The American Dental Association Does Not Recognize Cosmetic Dentistry As A Formal Specialty Area Of Dentistry.However, There Are Still Dentists That Promote Themselves As Cosmetic Dentists.</p>

            <h1>Types</h1>
            <hr>
            <ol>1.the addition of a dental material to teeth or gums – examples: bonding, porcelain veneers (laminates), crowns (caps), gum grafts</ol>
            <ol>2.the removal of tooth structure or gums – examples: enameloplasty, gingivectomy</ol>
            <ol>3.neither adding nor removing dental materials, tooth structure, or gums – examples: teeth whitening (bleaching), laser whitening, gum depigmentation</ol>
            <ol>4.straightening of teeth accompanied by improvement in appearance of face</ol>
            <ol>5.veneers, dental laminates - conservative</ol>
            <ol>6.scaling</ol>

            <h1>Whitening</h1>
            <hr>
            <p>Whitening, or "tooth bleaching", is the most common cosmetic dental procedure. Whitening is a safe process that is effective for most patients.[7] Multiple whitening options are available, including over the counter products such as Crest Whitestrips, as well as dentist-supervised methods such as in-office treatments or at-home treatments involving trays with a peroxide gel.</p>
            <p>Laser whitening is a teeth whitening technique in which gums are covered with a rubber dam and a bleaching chemical is applied on the teeth. A beam of argon laser, which is intended to accelerate the process of bleaching, is then projected upon the teeth. This laser activates the bleaching chemical and lightens the teeth color.[9] Laser whitening is said to be six times more effective in teeth whitening compared to other procedures.</p>

            <h1>Reshaping</h1>
            <hr>
            <p>Tooth reshaping removes parts of the enamel to improve the appearance of the tooth. It may be used to correct a small chip, or to alter the length, shape or position of teeth, as well as when there is tooth size discrepancy; it can be used to correct crooked or excessively long teeth. The removed enamel is irreplaceable, and may sometimes expose dentin. It is also known as enameloplasty, odontoplasty, contouring, recontouring, cosmetic contouring, slenderizing, stripping. This procedure offers fast results and can even be a substitute for braces under certain circumstances.</p>
            
            <h1>Bonding</h1>
            <hr>
            <p>Bonding is a process in which an enamel-like dental composite material is color matched, applied to a tooth's surface, sculpted into proper tooth contour, hardened and then polished. An examples of a bonded restorations are inlays and onlays, which are used to repair decayed & cracked teeth. Teeth damaged by small to moderate decay, erosion, or small fractures can be repaired with direct composite restorations bonded to the teeth. Aesthetics are especially critical in anterior composite restorations.</p>

            <h1>Bridging</h1>
            <hr>
            <p>Dental bridges are used to replace one or more missing teeth. Teeth on both sides of the space left by the missing teeth are prepared.A bridge is made up of abutments, the teeth that are prepped, and the missing, false teeth, which are called pontics.This procedure is used to replace one or more missing teeth and is cemented in. Bridges can consist of more than three teeth in total and the viability of any bridge is usually determined by applying Ante's Law and assessing where in the mouth the teeth are.[16] Most bridges are fixed, they can not be removed. Fixed bridges cannot be taken out in the same way that partial dentures can. In areas of the mouth that are under less stress, such as the front teeth, a cantilever bridge may be used. Cantilever bridges or Maryland Bridges are used to replace anterior or front missing teeth, are minimally invasive but do have a reputation for failure.Bridges require commitment to serious oral hygiene and carry risk.The average life of bridges is similar to that of crowns which is nearly ten years.</p>
        
            <h1>Veneers</h1>
            <hr>
            <p>Veneers are ultra-thin, custom-made porcelain laminates that are bonded directly to the teeth. They are an option for closing gaps, enhance the shape, or change the color of teeth that do not respond well to whitening procedures. In the majority of the cases, some level of tooth reduction is necessary for optimal results. To achieve a pleasing smile, dentists fabricate diagnostic mock-ups, which act as a tooth preparation guide before the fabrication of veneers, it allows the dentist to visualize the changes needed to be done on the patient's teeth with respect to size, shape and proportion, its relation with gingival-contour, lip contour and smile line. Mock ups can be a great aid in fabricating preevaluation temporaries, which can give a preliminary evaluation to the esthetics, phonetics and teeth occlusion, in addition, it gives an opportunity to the clinicians to check if they need to do any corrections.</p>
        
            <h1>Implants</h1>
            <hr>
            <p>Dental implants are prosthetic replacements for missing teeth. According to ICOI (International Congress of Oral Implantologists) there are commonly three parts to what it is described as an implant: the implant device which is predominantly made of titanium (which is inserted into the bone), the abutment, and a dental crown or a denture which are connected to the implant through the abutment.</p>

            <h1>Gum lifts</h1>
            <hr>
            <p>A gum lift is a cosmetic dental procedure that raises and sculpts the gum line. The procedure involves reshaping the tissue and/or underlying bones to create the appearance of longer or more symmetrical teeth.</p>

            <h1>Straightening</h1>
            <hr>
            <p>Metal braces are used primarily to correct crowded or misaligned teeth. While correcting issues such as jaw alignment are the primary purpose, they can also improve aesthetics.
            Clear aligners are used as an alternative to traditional metal braces. Clear aligners produce comparable results to metal braces and are preferred by some patients as they are less noticeable to others than traditional braces.</p>

            <h1>Bite reclamation</h1>
            <hr>
            <p>Bite reclamation is for patients who have had years of excessive wear to their teeth due to grinding or acid reflux can alter their vertical dimension. This gives them a closed or shorter look to their face and smile. By opening up their bite, a qualified professional can reclaim their vertical dimension.</p>
        
        
        
