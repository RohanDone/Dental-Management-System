            <p class="para">Cosmetic dentistry is generally used to refer to any dental work that improves the appearance (though not necessarily the functionality) of teeth, gums and/or bite.
                It primarily focuses on improvement in dental aesthetics in color, position, shape, size, alignment and overall smile appearance.
                Many dentists refer to themselves as "cosmetic dentists" regardless of their specific education, specialty, training,
                and experience in this field. This has been considered unethical with a predominant objective of marketing to patients.
                The American Dental Association does not recognize cosmetic dentistry as a formal specialty area of dentistry.
                However, there are still dentists that promote themselves as cosmetic dentists.</p>
            <p>Cosmetic dentistry may involve:

                the addition of a dental material to teeth or gums – examples: bonding, porcelain veneers (laminates), crowns (caps), gum grafts
                the removal of tooth structure or gums – examples: enameloplasty, gingivectomy
                neither adding nor removing dental materials, tooth structure, or gums – examples: teeth whitening (bleaching), laser whitening, gum depigmentation
                straightening of teeth accompanied by improvement in appearance of face – orthodontics
                veneers, dental laminates - conservative
                scaling</p>
